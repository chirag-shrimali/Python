# Take a number as input , convert it to a float , and print both the original and converted values with their data types...

no = input("Enter the No : ") # <class 'str'>

con = float(no) # <class 'float'>

# ---------------------------------------------------------------------------

print(no) # user enters number as string data types print

print(type(no)) # <class 'str'>

print(con) # converted data types string to float prints as user enters

print(type(con)) # <class 'float'>