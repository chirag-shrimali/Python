# Write a python program that takes a user's name as input and prints :-

"""
1. The First Characters of the String
2. The Last Characters of the String
3. The Total Length of the String Name
"""

name = input("\nEnter Your Name : ")

print("\nThe First Character is :",name[0]) # First Character

print("\nThe Last Character is :",name[len(name) - 1]) # Last Character

print("\nThe Length of Your Name is :",len(name)) # Print Length of the String