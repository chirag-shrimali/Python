# Calculate the Average of a two number that can be enters by user

no1 = int(input("Enter the No1 : ")) # Takes Integers Values from the users and storing(assign) to the no1 as variables

no2 = int(input("Enter the No2 : ")) # Takes Integers Values from the users and storing(assign) to the no2 as variables

