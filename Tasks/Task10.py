# Calculate the square of a number that can be enters by user

# Area of squares are : l * l
# Circumference(Perimeter) of Squares are : 4 * l

# Calculate the area of square

length = float(input("Enter the Length of a Squares : "))

area = length * length # also write length * * 2 but length ^ 2 not writes

perimeter = 4 * length

print("The Perimeter(Circumference) is : ",perimeter,"\bcm")

print("The Area of Squares are : ",area,"\bcm2")